/**
 * Creates .env for Prisma CLI from .env.local (both gitignored).
 * Rewrites Supabase URLs to transaction pooler (6543) + session pooler (5432).
 */
import fs from 'node:fs';
import path from 'node:path';

const root = path.resolve(import.meta.dirname, '..');
const localPath = path.join(root, '.env.local');
const envPath = path.join(root, '.env');

function parseEnvFile(content) {
  const vars = {};
  for (const line of content.split('\n')) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith('#')) continue;
    const eq = trimmed.indexOf('=');
    if (eq === -1) continue;
    const key = trimmed.slice(0, eq).trim();
    let value = trimmed.slice(eq + 1).trim();
    if (
      (value.startsWith('"') && value.endsWith('"')) ||
      (value.startsWith("'") && value.endsWith("'"))
    ) {
      value = value.slice(1, -1);
    }
    vars[key] = value;
  }
  return vars;
}

function projectRefFromSupabaseUrl(url) {
  const match = url?.match(/https:\/\/([^.]+)\.supabase\.co/);
  return match?.[1] ?? null;
}

function passwordFromPostgresUrl(url) {
  if (!url) return null;
  try {
    const parsed = new URL(url.replace(/^postgresql:/, 'http:'));
    return parsed.password ? decodeURIComponent(parsed.password) : null;
  } catch {
    return null;
  }
}

function poolerHostFromUrl(url) {
  const match = url?.match(/@([a-z0-9-]+\.pooler\.supabase\.com)/i);
  return match?.[1] ?? 'aws-1-us-east-1.pooler.supabase.com';
}

function buildSupabasePrismaUrls(local) {
  const ref = projectRefFromSupabaseUrl(local.NEXT_PUBLIC_SUPABASE_URL);
  const password =
    passwordFromPostgresUrl(local.DIRECT_URL) ?? passwordFromPostgresUrl(local.DATABASE_URL);

  if (!ref || !password) {
    throw new Error(
      'Need NEXT_PUBLIC_SUPABASE_URL plus DATABASE_URL or DIRECT_URL with password in .env.local'
    );
  }

  const host = poolerHostFromUrl(local.DATABASE_URL) ?? poolerHostFromUrl(local.DIRECT_URL);
  const encoded = encodeURIComponent(password);

  return {
    DATABASE_URL: `postgresql://postgres.${ref}:${encoded}@${host}:6543/postgres?pgbouncer=true&sslmode=require`,
    DIRECT_URL: `postgresql://postgres.${ref}:${encoded}@${host}:5432/postgres?sslmode=require`,
  };
}

if (!fs.existsSync(localPath)) {
  console.error('Missing .env.local — copy .env.example and fill in Supabase values.');
  process.exit(1);
}

const local = parseEnvFile(fs.readFileSync(localPath, 'utf8'));
const { DATABASE_URL, DIRECT_URL } = buildSupabasePrismaUrls(local);

const contents = `# Auto-generated by pnpm db:env:sync — Prisma CLI only (gitignored)
# Edit .env.local, then run: pnpm db:env:sync
DATABASE_URL="${DATABASE_URL}"
DIRECT_URL="${DIRECT_URL}"
`;

fs.writeFileSync(envPath, contents, 'utf8');
console.log(`Wrote ${path.basename(envPath)}`);
console.log('  DATABASE_URL → transaction pooler :6543');
console.log('  DIRECT_URL   → session pooler :5432');
